
public class Strings {
	
	public static void main(String[] args) {
		
		// Declare string
		String[] words = new String[3];
		// enter values of string
		words[0] = "Hello";
		words[1] = "to";
		words[2] = "you";
		
		// Declare and define string sametime
		String[] fruits = {"apple", "banana", "pear", "kiwi" };
		
		// Short-form FOR loop
		for(String fruit: fruits) {
			System.out.println(fruit);
		}
		
	//	int value = 0;
		
		// Default value to references is NULL
		// NULL - this means reference exists but it points to nothing
	// String text = null;
		
		String[] texts = new String[2];
		
		texts[0] = "one";
			
	}

}
